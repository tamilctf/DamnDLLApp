namespace AddDLL
{
    public class Calculator
    {
        /// <summary>
        /// Adds two integers and returns the result.
        /// </summary>
        /// <param name="x">First integer.</param>
        /// <param name="y">Second integer.</param>
        /// <returns>The sum of x and y.</returns>
        public int AddNumbers(int x, int y)
        {
            return x + y;
        }
    }
}
